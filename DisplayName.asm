
org 100h

.MODEL SMALL
.STACK 100h
.DATA
    MSG1 DB "Enter Your Name $"
    ARR DB 1O0dup("$")

.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DX,AX
	MOV SI,OFFSET ARR
	
	DISPLAY_STRING:
	LEA DX,MSG1
	MOV AH,9
	INT 21h
	
	INPUT_NAME:
	MOV AH,8
	INT 21h
	CMP AL,36
	JE PROGRAM_END
	MOV [SI],AL
	INC SI
	JMP INPUT_NAME
	
	PROGRAM_END:
	MOV DX,OFFSET ARR
	MOV AH,9
	INT 21h
	MOV AH,4Ch
	INT 21h
	ENDP
	END MAIN

ret




